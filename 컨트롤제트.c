#include <stdio.h>
#include <stdbool.h>
#include <stdlib.h>

// 파라미터로 주어지는 문자열은 const로 주어집니다. 변경하려면 문자열을 복사해서 사용하세요.
int solution(const char* s) {
    int answer = 0;
    char* token = strtok((char*)s, " ");
    int pre_num = 0;

    while (token != NULL) {
        if (strcmp(token, "Z") == 0) {
            answer -= pre_num;
        }
        else {
            int now = atoi(token);  // 문자열을 정수로 변환 atoi
            answer += now;
            pre_num = now;
        }
        token = strtok(NULL, " ");
    }

    return answer;
}